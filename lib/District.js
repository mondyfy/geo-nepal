class District {
	constructor(id, name, nepaliName, headquaters, area, population, province) {
		this.id = id;
		this.name = name;
		this.nepaliName = nepaliName;
		this.area = area;
		this.headquaters = headquaters;
		this.population = population;
		this.province = province;
	}
}
module.exports = District;